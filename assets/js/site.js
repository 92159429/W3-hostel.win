document.addEventListener('DOMContentLoaded', () => {
  const saved = localStorage.getItem('w3-lang') || 'zh-Hant';
  setLang(saved);
  injectFloatingBooking();

  document.querySelectorAll('.lang-switch button').forEach(btn => {
    btn.addEventListener('click', () => setLang(btn.dataset.lang));
  });

  const revealObserver = new IntersectionObserver(entries => {
    entries.forEach(entry => {
      if (entry.isIntersecting) entry.target.classList.add('visible');
    });
  }, { threshold: 0.12 });

  document.querySelectorAll('.reveal').forEach(el => revealObserver.observe(el));

  const topbar = document.querySelector('.topbar');
  const sceneEls = document.querySelectorAll('.page-hero, .parallax, .multi-bg, .blend-bg, .card, .section-head');
  const hero = document.querySelector('.hero');
  const heroSlides = document.querySelector('.hero .slides');
  const heroContent = document.querySelector('.hero-content');
  const pageHero = document.querySelector('.page-hero');
  const pageHeroInner = document.querySelector('.page-hero .container');
  const reducedMotion = window.matchMedia('(prefers-reduced-motion: reduce)').matches;

  document.querySelectorAll('main section').forEach((section, idx, arr) => {
    if (idx < arr.length - 1 && !section.nextElementSibling?.classList?.contains('scene-divider')) {
      const divider = document.createElement('div');
      divider.className = 'scene-divider';
      section.insertAdjacentElement('afterend', divider);
    }
  });

  function clamp(value, min, max) {
    return Math.min(Math.max(value, min), max);
  }

  function updateScenes() {
    const scrollY = window.scrollY || window.pageYOffset;
    const viewportH = window.innerHeight || 1;
    document.documentElement.style.setProperty('--scroll-y', `${scrollY}px`);

    if (topbar) topbar.classList.toggle('is-scrolled', scrollY > 12);

    if (!reducedMotion && hero) {
      const hr = hero.getBoundingClientRect();
      const progress = clamp((viewportH - hr.top) / (viewportH + hr.height), 0, 1.25);
      const shift = Math.round(progress * 110);
      const scale = 1 + progress * 0.06;
      document.documentElement.style.setProperty('--hero-shift', `${shift}px`);
      document.documentElement.style.setProperty('--hero-scale', scale.toFixed(4));
      document.documentElement.style.setProperty('--hero-content-shift', `${Math.round(progress * 56)}px`);
      if (heroSlides) heroSlides.style.opacity = `${1 - progress * 0.18}`;
      if (heroContent) heroContent.style.opacity = `${1 - progress * 0.1}`;
    }

    if (!reducedMotion && pageHero) {
      const pr = pageHero.getBoundingClientRect();
      const progress = clamp((viewportH - pr.top) / (viewportH + pr.height), 0, 1.3);
      const shift = Math.round(progress * 72);
      const scale = 1 + progress * 0.03;
      document.documentElement.style.setProperty('--hero-shift', `${shift}px`);
      document.documentElement.style.setProperty('--hero-scale', scale.toFixed(4));
      document.documentElement.style.setProperty('--hero-content-shift', `${Math.round(progress * 18)}px`);
      if (pageHeroInner) pageHeroInner.style.opacity = `${1 - progress * 0.08}`;
    }

    sceneEls.forEach(el => {
      const r = el.getBoundingClientRect();
      const center = r.top + r.height / 2;
      const distance = (viewportH / 2 - center) / viewportH;
      const depth = clamp(1 - Math.abs(distance * 2.1), 0, 1);
      const shift = Math.round(distance * 90);
      const tilt = (distance * 7).toFixed(2);

      el.style.setProperty('--scene-depth', depth.toFixed(3));
      el.style.setProperty('--scene-shift', `${shift}px`);
      el.style.setProperty('--scene-tilt', `${tilt}deg`);
      el.classList.toggle('scene-active', depth > 0.02);
    });
  }

  updateScenes();
  window.addEventListener('scroll', updateScenes, { passive: true });
  window.addEventListener('resize', updateScenes);
});
