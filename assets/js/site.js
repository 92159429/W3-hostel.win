function setLang(lang) {
  const t = translations[lang] || translations['zh-Hant'];
  document.documentElement.lang = lang;
  localStorage.setItem('w3-lang', lang);
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const path = el.dataset.i18n.split('.');
    let value = t;
    path.forEach(k => value = value?.[k]);
    if (typeof value === 'string') el.textContent = value;
  });
  document.querySelectorAll('[data-nav-index]').forEach(el => {
    const idx = Number(el.dataset.navIndex);
    if (t.nav[idx]) el.textContent = t.nav[idx];
  });
  document.querySelectorAll('.lang-switch button').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

