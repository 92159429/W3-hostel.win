function getValueByPath(obj, path) {
  return path.split(".").reduce((acc, key) => acc?.[key], obj);
}

function setTextTranslations(t) {
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const value = getValueByPath(t, el.dataset.i18n);
    if (typeof value === "string") {
      el.textContent = value;
    }
  });

  document.querySelectorAll("[data-nav-index]").forEach((el) => {
    const idx = Number(el.dataset.navIndex);
    if (t.nav?.[idx]) el.textContent = t.nav[idx];
  });
}

function setButtonState(lang) {
  document.querySelectorAll(".lang-switch button").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === lang);
  });
}

function applyMetaByPage(t) {
  const path = window.location.pathname;
  let pageKey = "home";

  if (path.endsWith("/w3.html") || path.endsWith("w3.html")) pageKey = "about";
  else if (path.endsWith("/space.html") || path.endsWith("space.html")) pageKey = "space";
  else if (path.endsWith("/travel.html") || path.endsWith("travel.html")) pageKey = "travel";
  else if (path.endsWith("/contact.html") || path.endsWith("contact.html")) pageKey = "contact";

  const page = t[pageKey];
  if (!page) return;

  if (page.metaTitle) document.title = page.metaTitle;

  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc && page.metaDescription) metaDesc.setAttribute("content", page.metaDescription);

  const metaKeywords = document.querySelector('meta[name="keywords"]');
  if (metaKeywords && page.metaKeywords) metaKeywords.setAttribute("content", page.metaKeywords);

  const ogTitle = document.querySelector('meta[property="og:title"]');
  if (ogTitle && page.ogTitle) ogTitle.setAttribute("content", page.ogTitle);

  const ogDesc = document.querySelector('meta[property="og:description"]');
  if (ogDesc && page.ogDescription) ogDesc.setAttribute("content", page.ogDescription);

  document.documentElement.lang = document.documentElement.lang || "zh-Hant";
}

function setLang(lang) {
  const finalLang = translations[lang] ? lang : "zh-Hant";
  const t = translations[finalLang];

  document.documentElement.lang = finalLang;
  localStorage.setItem("w3-lang", finalLang);

  const params = new URLSearchParams(window.location.search);
  params.set("lang", finalLang);
  history.replaceState(null, "", `${window.location.pathname}?${params.toString()}`);

  setTextTranslations(t);
  setButtonState(finalLang);
  applyMetaByPage(t);
}

function revealEmail(button) {
  const user = button.dataset.user;
  const domain = button.dataset.domain;
  if (!user || !domain) return;

  const email = `${user}@${domain}`;
  const link = document.createElement("a");
  link.href = `mailto:${email}`;
  link.textContent = email;

  button.replaceWith(link);
}

function revealPhone(button) {
  const p1 = button.dataset.part1;
  const p2 = button.dataset.part2;
  const p3 = button.dataset.part3;
  if (!p1 || !p2 || !p3) return;

  const phone = `${p1}-${p2}-${p3}`;
  const tel = `${p1}${p2}${p3}`;

  const link = document.createElement("a");
  link.href = `tel:${tel}`;
  link.textContent = phone;

  button.replaceWith(link);
}

